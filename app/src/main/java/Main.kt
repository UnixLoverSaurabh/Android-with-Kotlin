fun main(args: Array<String>) {
    println("Hello World")

    // Variables and Types
    val country : String = "India"
    println("My country is $country")

    var code : Int = 805102
    println("City code is $code")
    code = 805110
    println("City code is $code")

    // val and var on declarations
    // val : value      reassignment not possible
    // var : variable   new value can be assigned
}